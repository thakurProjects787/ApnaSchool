public class ShowUserAllTask {

    private String taskID = "";
    private String taskType = "";
    private String taskHeading = "";
    private String taskLastDate = "";

    public ShowUserAllTask() {
    }

    public String getTaskID() {
        return taskID;
    }

    public void setTaskID(String taskID) {
        this.taskID = taskID;
    }

    public String getTaskType() {
        return taskType;
    }

    public void setTaskType(String taskType) {
        this.taskType = taskType;
    }

    public String getTaskHeading() {
        return taskHeading;
    }

    public void setTaskHeading(String taskHeading) {
        this.taskHeading = taskHeading;
    }

    public String getTaskLastDate() {
        return taskLastDate;
    }

    public void setTaskLastDate(String taskLastDate) {
        this.taskLastDate = taskLastDate;
    }


}
