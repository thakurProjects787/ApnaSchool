package com.projects.thakur.apnaschool.Model;



public class MDMDetails {

    private String total_students = "";
    private String diet_menu_details = "";
    private String rice_stock = "";
    private String other_stock = "";

    public MDMDetails() {
    }

    public String getTotal_students() {
        return total_students;
    }

    public void setTotal_students(String total_students) {
        this.total_students = total_students;
    }

    public String getDiet_menu_details() {
        return diet_menu_details;
    }

    public void setDiet_menu_details(String diet_menu_details) {
        this.diet_menu_details = diet_menu_details;
    }

    public String getRice_stock() {
        return rice_stock;
    }

    public void setRice_stock(String rice_stock) {
        this.rice_stock = rice_stock;
    }

    public String getOther_stock() {
        return other_stock;
    }

    public void setOther_stock(String other_stock) {
        this.other_stock = other_stock;
    }


}
