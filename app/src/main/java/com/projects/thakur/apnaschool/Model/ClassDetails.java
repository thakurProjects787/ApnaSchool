package com.projects.thakur.apnaschool.Model;



public class ClassDetails {

    private String class_name="";
    private String total_students="";
    private String class_agenda="";

    public ClassDetails() {

    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public String getTotal_students() {
        return total_students;
    }

    public void setTotal_students(String total_students) {
        this.total_students = total_students;
    }

    public String getClass_agenda() {
        return class_agenda;
    }

    public void setClass_agenda(String class_agenda) {
        this.class_agenda = class_agenda;
    }

}
